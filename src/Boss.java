public class Boss {

    private int hils;
    private int deimege;
    private String sit;

    public Boss(int hils, int deimege, String sit) {
        this.hils = hils;
        this.deimege = deimege;
        this.sit = sit;
    }
    public int getHils() {
        return hils;
    }
    public int getDeimege() {
        return deimege;
    }
    public String getSit(){
        return sit;
    }
    public void setHils(int hils) {
        this.hils = hils;
    }
    public void setDeimege(int deimege) {
        this.deimege = deimege;
    }
    public void setSit(String sit) {
        this.sit = sit;
    }
}
